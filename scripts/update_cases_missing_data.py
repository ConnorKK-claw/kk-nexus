from pathlib import Path
import os

vault = Path(os.environ["USERPROFILE"]) / ".codex" / "skills" / "equity-incentive" / "vault"
cases_dir = vault / "cases"

def update_file(fname, old, new):
    fp = cases_dir / fname
    c = fp.read_text(encoding="utf-8")
    if old in c:
        c = c.replace(old, new)
        fp.write_text(c, encoding="utf-8")
        print(f"[OK] {fname}: 已替换")
    else:
        print(f"[WARN] {fname}: 未找到替换文本")

def append_section(fname, marker, section):
    fp = cases_dir / fname
    c = fp.read_text(encoding="utf-8")
    if marker in c and section.strip() not in c:
        c = c.replace(marker, section + "\n" + marker)
        fp.write_text(c, encoding="utf-8")
        print(f"[OK] {fname}: 已追加内容")
    elif section.strip() in c:
        print(f"[SKIP] {fname}: 内容已存在")
    else:
        print(f"[WARN] {fname}: 标记 '{marker}' 未找到")

